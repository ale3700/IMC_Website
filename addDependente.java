    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String nome = CampoNome.getText();
    String cpf = CampoCPF.getText();
    String numero = CampoNumero.getText();

       
         if (mapaDependentes == null) {
        System.out.println("Erro: mapaDependentes é null.");
        return;
        }
          Dependente dependente = Dependente.criarDependente(nome, cpf, numero);
       
        if (dependente == null) {
        CampoNome.setText("");
        CampoCPF.setText("");
        CampoNumero.setText("");
    } else {
        mapaDependentes.put(cpf, dependente);

        System.out.println("Dependente adicionado com sucesso!");

        this.setVisible(false);
        this.dispose();
    }
    
    
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
